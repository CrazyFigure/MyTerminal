export type TranslationKey = keyof (typeof messages)['zh-CN'];

const statusKeyMap = {
  idle: 'statusIdleLabel',
  connecting: 'statusConnectingLabel',
  connected: 'statusConnectedLabel',
  stub: 'statusStubLabel',
  error: 'statusErrorLabel',
  closed: 'statusClosedLabel',
  running: 'statusRunningLabel',
  stopped: 'statusStoppedLabel',
} as const satisfies Record<string, TranslationKey>;

export const translate = (language: UiLanguage, key: TranslationKey, replacements?: Replacements) => {
  const template: string = messages[language][key] ?? messages['zh-CN'][key];
  if (!replacements) {
    return template;
  }

  return Object.entries(replacements).reduce<string>((result, [placeholder, value]) => {
    return result.replaceAll(`{${placeholder}}`, String(value));
  }, template);
};

export const translateStatus = (language: UiLanguage, status: string) => {
  const key = statusKeyMap[status as keyof typeof statusKeyMap] ?? 'statusIdleLabel';
  return translate(language, key);
};

export const toggleLanguageValue = (language: UiLanguage): UiLanguage =>
  language === 'zh-CN' ? 'en-US' : 'zh-CN';
